<p>Hunde har været en del af menneskets hverdag i tusindvis af år. Nye studier har vist, at hunde der nedstammede fra den <a href="artikel/ulven">grå ulv</a> allerede blev tæmmet for 130.000 år siden. Det er lang tid siden og langt fra de <strong>hunderacer</strong> vi kender til den dag i dag.</p>

<p>I forhistoriske tider var det primære mål, at opdrætte hunde der var fysisk såvel som mentalt stærke, så de kunne passe det job de nu blev fremavlet til.</p>

<p>I moderne tid er langt de fleste hunde et familiemedlem som ikke har nogen reel arbejdsfunktion, men for tusinder af år siden var det dog anderledes – her blev hundene fremavlet med det formål, at de skulle kunne blive vagthunde, jagthunde eller hyrdehunde.</p>

<p>Efterhånden som tiden gik og mennesket udviklede sig gjorde hundene det samme. Deciderede hunderacer opstod, som havde specifikke arbejdsfunktioner – skræddersyet til det arbejde de nu blev opdraget/fremavlet til.</p>

<h2>Hvad er en hunderace?</h2>

<p>Her kan vi tage udgangspunkt i to forskellige måder at definere en hunderace på. Fra en opdrætters synspunkt er en hunderace en <strong>letgenkendelig hund</strong>, hvor de samme fysiske og mentale egenskaber kan genskabes. Her skal der selvfølgelig tages højde for, at ingen hunderace kan genskabes 100% eller tilnærmelsesvis der omkring – via avl.</p>

<p>Hvis vi antager at to schæferhunde parrer sig, så vil vi se en <a href="schaeferhund">schæferhund</a> med mange af de samme træk – pelsen kan variere, men kropsbygningen, posituren, ansigtet, formen – hele hunden som helhed kan med sikkerhed igen defineres som værende en schæferhund.</p>

<p>Antager man at en schæferhund og en <a href="golden-retriever">golden retriever</a> parrer sig, så vil du få et ukendt resultat der ikke har nogen <strong>racestandard</strong>. Her kan det være, at du vil få en hund der har posituren som en golden retriever, ørerne som en schæfer og mentaliteten som en golden retriever.</p>

<h2>Racerene hunde</h2>

<p>Ved en såkaldt <strong>raceren hund</strong>, vil man oftest føre det der hedder en stamtavle. En stamtavle er ligesom et stamtræ, der kan gå meget langt tilbage. Her vil man kunne se eventuelle sygdomme og helbredsproblemer.</p>

<p>Du vil kunne se farver og titler. Det kan være, at der i den pågældende hunds familie er tendens til eksempelvis hofteledsdysplasi (HD), albueledsdysplasi (AD), osteochondritis dissecans (OCD) eller andre sygdomme. Her kan man så fravælge en eventuel hvalp ved køb, hvis ikke man tænker, at hvalpen kan komme til, at lide af for mange skavanker.</p>

<h2>Hvor mange hunderacer findes der?</h2>

<p>Det kommer an på hvilken hundeorganisation man taler om. <strong>Den internationale hundeorganisation</strong> <a href="artikel/fci">FCI</a> (Fédération Cynologique Internationale) har listet 353 anerkendte hunderacer. Den amerikanske kennel klub - AKC (American Kennel Club) - har blot anerkendt 197 hunderacer.</p>

<p>Der er flere forskellige internationale hundeorganisationer rundt omkring i verden og de alle har forskellige optagelseskrav. Der er også mange hunde som står på venteliste for, at blive godkendt. I Danmark anser vi eksempelvis <strong>racen Dansk Spids</strong>, som værende en hunderace. Men den lever på nuværende tidspunkt ikke op til kravene fra FCI. Når den Danske Spids engang lever op til kravene, så kan der ansøges om optagelse på FCI.</p>

<h2>10 grupper for alle slags hunde</h2>

<p>FCI er den liste vi i Danmark følger. FCI opdeler de <strong>anerkendte hunderacer</strong> i 10 kategorier;</p>

<ul>
	<li>Gruppe 1 – hyrde og kvæghunde</li>
	<li>Gruppe 2 – schnauzere, pinschere, molosser og sennenhunde</li>
	<li>Gruppe 3 – terriere</li>
	<li>Gruppe 4 – gravhunde</li>
	<li>Gruppe 5 – spidshunde</li>
	<li>Gruppe 6 – drivende jagthunde og schweisshunde</li>
	<li>Gruppe 7 – stående jagthunde</li>
	<li>Gruppe 8 – apporterende jagthunde</li>
	<li>Gruppe 9 – selskabshunde</li>
	<li>Gruppe 10 – mynder</li>
</ul>

<p>AKC og FCI er meget ens på mange områder, men der er dog områder hvor de skiller sig markant ud. <strong>FCI forbyder eksempelvis at kupere halen på en hund</strong>, hvorimod AKC har en lidt lempeligere tilgang til <a href="artikel/halekupering">halekupering</a>. At kupere en hunds hale betyder, at man skærer et stykke af hundens hale.</p>

<p>I Danmark er det lovligt at halekupere 5 hunderacer. Hunderacerne skal have stamtavle og indgrebet må kun foretages af en dyrlæge i det meget tidlige hvalpestadie (højest 4 dage gammel). Følgende <strong>hunderacer</strong> må halekuperes i Danmark;</p>

<ul>
	<li><a href="ruhaaret-hoensehund">Ruhåret hønsehund</a></li>
	<li><a href="korthaaret-hoensehund">Korthåret hønsehund</a></li>
	<li><a href="weimaraner">Weimaraner</a></li>
	<li><a href="vizsla-ruhaaret">Vizla</a></li>
	<li><a href="breton">Breton</a></li>
</ul>

<p>Begrundelsen for, at disse hunderacer må halekuperes skyldes tilsyneladende, at de varetager arbejdsopgaver som kan skade deres hale.</p>

<h2>Vi vil gerne afdække alle hunderacer</h2>

<p>Vores mission er, at vi gerne vil lave raceportrætter af alle hunderacer. Det er en stor opgave, da det kræver meget research, at afdække alle disse hunderacer. Alle hunderacer på siden vil være internationalt anerkendt, da vi kun skriver om de hunde der er listet på den liste som FCI har godkendt.</p>

<h2>Populære hunderacer</h2>

<p>I Danmark findes der mange populære hunderacer. Dansk Kennel Klub (DKK) laver hvert år en liste, der viser antallet af nye registreringer. Listen omfatter hvalpe som er født i Danmark samt importerede voksne hunde- og hvalpe med FCI stambøger fra andre lande, der har fået tildelt et DKK stambogsnummer.</p>

<p>Dansk Kennel Klub skriver, at de stambogsfører knap 40% af årets nye hvalpe i Danmark. Det betyder, at statistikken herunder kun er baseret på de mange racer der har en stambog.</p>

<h3>De 10 mest populære hunderacer registreret i 2020</h3>

<h4>Labrador Retriever - 2921</h4>

<p>Listen af de mest populære hunderacer bliver domineret af Labrador Retrieveren. Det er ikke så sært, at det er en af de mest kendte hunderacer herhjemme. Den er nemlig kendt for, at være en <strong>fantastisk familiehund</strong> der virkelig drager omsorg for alle omkring sig.</p>

<h4>Schæferhund, stockhåret - 1429</h4>

<p>Schæferen kommer på en flot andenplads på listen. Selvom det er den <strong>mest udbredte brugshund i verdenen</strong>, så er det også en ganske populær familiehund. Det er en loyal hunderace der kræver meget motion, men hvis man har en aktiv familie, så kan schæferhunden være det perfekte valg.</p>

<h4>Golden retriever - 1396</h4>

<p>Golden retrieveren er en skøn hund. Det er en klassisk familiehund der elsker sin ejer. Den er intelligent, lydig og enormt charmerende. <strong>Golden Retrieveren elsker mennesker</strong> og vil passe godt ind i de fleste børnefamilier.</p>

<h4>Bichon Havanais - 1349</h4>

<p>Bichon Havanais (også kaldet Havaneser) er en sød hund der går under kategorien <strong>små hunde</strong>. Det er én af de mest populære hunde i Danmark og det skyldes formentlig, at det er en lille hunderace der med sit legesyge sind, charmerer sig ind på enhver den måtte møde på sin vej.</p>

<h4>Staffordshire bull terrier - 791</h4>

<p>Staffordshire bull terrieren er en mellemstor hund der trods sit robuste og til tider hårde udseende, er en utrolig <strong>venlig og kærlig hunderace</strong>. Det er ikke en hund der passer ind i enhver familie, da den kræver en ejer og en familie der forstår hvilken type hund det er og hvilket temperament hunden har.</p>

<h4>Coton de tulear - 638</h4>

<p>Racen Coton de tulear hører til under det vi kalder små hunderacer. Racen bliver også kaldet en <strong>bomuldshund</strong>, hvilket skyldes dens smukke og bomuldsagtige pels. En Coton de tulear kommer normalt godt ud af det med alle i hjemmet - uanset om dette er børn eller andre husdyr.</p>

<h4>Cavalier king charles spaniel - 500</h4>

<p>En Cavalier king charles spaniel er en klassisk familiehund. Det er en loyal følgesvend der går dig lige i hælene hvor end du bevæger dig hen - også på toilettet. Racen har et langt navn og bliver tit bare <strong>omtalt som Cavalier</strong>. </p>

<h4>Råhåret hønsehund - 416</h4>

<p>Oprindeligt stammer den Ruhårede hønsehund fra Tyskland. Det er en særdeles <strong>populær jagthund</strong>. Grundet sit jagtinstinkt og sin nysgerrighed er det en meget aktiv hund. Racen er også meget velegnet som familiehund, hvor den gerne deltager i familiens gøremål. Den passer dog bedst ind i en meget aktiv familie der sørger for, at stille hundens behov.</p>

<h4>Engelsk Cocker spaniel - 381</h4>

<p>Den engelske cocker spaniel har sin oprindelse - som navnet nok fortæller - fra Storbritannien. Det er en race der er glad, nysgerrig, kærlig og ikke mindst energisk. Den engelske cocker spaniel er en jagthund og hører til i FCI gruppe 8 – <strong>Apporterende jagthunde</strong>.</p>

<h4>Border Collie - 361</h4>

<p>Border collien lander på en flot tiendeplads. Det er én af verdens mest intelligente hunde - hvis ikke <strong>dén mest intelligente hund i verdenen</strong>. Det er en mellemstor hunderace der både er kærlig og legesyg. Den elsker sin ejer og er mere end villig til, at lære. Er man til agility, så vil en Border Collie absolut være et godt match.</p>

<h2>Menneskets bedste ven</h2>

<p>Hunden er som bekendt menneskets bedste ven. Det er ikke for sjov, at den har fået denne titel. Uanset hvilken dag du måtte have, så vil du aldrig blive skuffet når du ser din hund i dørkarmen når du kommer hjem. </p>

<p>Din hund vil altid møde dig med glæde og en logrende hale. Har du haft en dårlig dag, så kan du med garanti forvente, at den giver dig den ekstra opmærksomhed som du har brug for. </p>

<p>Da hunden for mange år siden blev fremavlet var idéen, at den primært skulle bruges til, at varetage forskellige opgaver - en såkaldt brugshund. Det viste sig dog hurtigt, at hunden kunne bruges til meget mere end det. </p>

<h2>Hunden kan hjælpe dig til et bedre helbred</h2>

<p>Ja, den er god nok. Der findes mange studier der viser, at når man ejer en <strong>hund</strong>, så kan en sideeffekt være, at du får et bedre helbred. Det er nemlig sådan, at når du ejer en hund, så vil du automatisk gå flere ture om dagen end du formentlig ellers ville - det betyder, at du får mere motion hver dag.</p>

<p>En hund kan også være med til, at forbedre din mentale tilstand. Du får ingen bedre modtagelse end den du får fra din hund når du kommer hjem. Har du en dårlig dag eller er der sket noget skidt i løbet af ugen og du bare er udkørt, så ved du, at din hund altid vil forsøge, at trøste dig og altid give dig den varme og glæde som du måske har brug for.</p>

<p>Vi mennesker har brug for kærlighed og omsorg - dette behøver ikke altid være fra et menneske, så også her vil en hund i nogle situationer kunne give dig det du har brug for. Der er også mange der finder glæde ved, at have én omkring sig hele tiden - her vil en hund altså også være en fantastisk kompagnon.</p>
